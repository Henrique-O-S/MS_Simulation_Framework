# -------------------------------------------------------------------------------------------------------------

class CarModel:
    def __init__(self, id, autonomy, price):
        self.id = id
        self.autonomy = autonomy
        self.price = int(price)
        
    # ---------------------------------------------------------------------------------------------------------

    def get_id(self):
        return self.id
    
    # ---------------------------------------------------------------------------------------------------------

    def get_autonomy(self):
        return self.autonomy
    
    # ---------------------------------------------------------------------------------------------------------

    def get_price(self):
        return self.price
    
    # ---------------------------------------------------------------------------------------------------------

    def __str__(self):
        return f"{self.id}, Autonomy: {self.autonomy}, Price: {self.price}"
    
# -------------------------------------------------------------------------------------------------------------